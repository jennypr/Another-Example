package lesson9;

public class ForLoop {
	
	public static void main(String[] args) {
		
		int num=1;
		
		while (num<=10) 
		{
			System.out.print(num+" ");
			num++;
			
		}
		
		System.out.println();
		
		for (int num2=1; num2<=10; num2++)
		{
			System.out.print(num2+" ");
		}
		System.out.println();
		System.out.println("The value of num is: "+num);
		
		
		
		
	}
	
	

}
