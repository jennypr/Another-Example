package lesson9;

public class Recap {

	public static void main(String[] args) {
		
		int count=1;
		while(count<=5) {
			System.out.println(count +" Good Morning");
			count++;
		}
		
		System.out.println("---------------------");
		
		int num=1;
		
		do {
			System.out.println(num+" Good Morning");
			num++;
			
		}while (num<=1);
		

	}

}
